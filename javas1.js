// Arithmetic operators:

var a=32;
var b=14;
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);


//Assignment operators

let c=10;
console.log(c);
c+=5;
console.log(c);
c-=5;
console.log(c);
c*=5;
console.log(c);
c/=5;
console.log(c);
c%=5;
console.log(c);


//Comparsion operators

let d=9;
console.log(d==9);
console.log(d!=9);
console.log(d===9);
console.log(d!==9);
console.log(d>9);
console.log(d>=9);
console.log(d<9);
console.log(d<=9);



// Logical operators

console.log(4>3&&2>1);
console.log(4<3&&2>1);

console.log(4<3||2>1);
console.log(4<3||2>1);

console.log(!true);
console.log(!false);

//Ternary operator

console.log(2==='2')? 'x':'y';
console.log(2===2)? 'x':'y';


//typeof Operator

console.log(typeof true);
console.log(typeof 2);
console.log(typeof '5');
console.log(typeof 5.4);